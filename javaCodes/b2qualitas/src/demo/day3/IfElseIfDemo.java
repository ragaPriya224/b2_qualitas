package demo.day3;

public class IfElseIfDemo {

	public static void main(String[] args) {
		int pen = 10;
		int money = 5;
		int pencil = 2;
		if(money > pen) {
			System.out.println("buy pen");
		}else if(money > pencil) {
			System.out.println("buy pencil");
		}else {
			System.out.println("return home");
		}

	}

}
